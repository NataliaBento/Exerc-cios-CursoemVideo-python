medida = float(input("Uma distância em metros: "))
cm = medida * 100
mm = medida * 1000
km = medida / 100
hm = medida / 1000
print(f"A medida de {medida} em centímetros é: {cm}cm")
print(f"A medida de {medida} em mílimetros é: {mm}")
print(f"A medida de {medida} em quilómetros é de: {km}")
print(f"A medida de {medida} em hm é de: {hm}")